    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        if(this.getBtnRegister().isSelected()){
            //register new user and login
                String userName = this.getTxtNewUsername().getText();
                String password = this.getPwdNewPassword().getText();
                String name = this.getTxtFullName().getText();
                if(loginController.validateFields(userName, password, name)){
 
                DB db = new DB();
                db.addUser(userName, password, name);
                loginController.loginUser(userName, password, this);
                // this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this, "Invalid Credentials");
            }
        }
        else{
            //login existing user
                String userName = this.getTxtUsername().getText();
                String password = this.getPwdPassword().getText();
        if(loginController.validateFields(userName, password)){

                loginController.loginUser(userName, password, this);
            }  
            else{
                JOptionPane.showMessageDialog(this, "Invalid Credentials");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

