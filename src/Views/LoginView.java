    private void btnRemoveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveUserActionPerformed
        if(this.getBtnRegister().isSelected()){
            JOptionPane.showMessageDialog(this, "Register Button cannot be selected when deleting a user.");
        }
        else{ //Must be a valid user to remove user
                String userName = this.getTxtUsername().getText();
                String password = this.getPwdPassword().getText();
                if(loginController.validateFields(userName, password)){

                //loginController.loginUser(userName, password, this);
                DB db = new DB();
                try {
                    UserController user = new UserController(db.getUser(userName, password));
                    if(user.validUser){
                        int reply = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete user "+userName +" ?");
                        if(reply == JOptionPane.YES_OPTION){
                             db.removeUser(user.userID);
                             JOptionPane.showMessageDialog(this, userName + "deleted.");
                        }
                        //POSSIBLY CLEAR FIELDS HERE?
                        
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(LoginView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }  
            else{
                JOptionPane.showMessageDialog(this, "Invalid Credentials");
            }        
        }
    }//GEN-LAST:event_btnRemoveUserActionPerformed

